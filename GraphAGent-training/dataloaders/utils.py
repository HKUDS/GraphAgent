import copy
from typing import Dict, Sequence, List


import transformers

from model.graph_action_agent import conversation as conversation_lib


IGNORE_INDEX = -100
DEFAULT_GRAPH_TOKEN = "<graph>"
DEFAULT_GRAPH_PATCH_TOKEN = "<g_patch>"
DEFAULT_G_START_TOKEN = "<g_start>"
DEFAULT_G_END_TOKEN = "<g_end>"


def preprocess_graph_Hetero(
    sources: Sequence[str],
    graph_cfg: dict,
    cur_token_lens: List[int],
) -> Dict:
    graph_token_lens = cur_token_lens

    for source in sources:

        for sentence in source:
            if DEFAULT_GRAPH_TOKEN in sentence["value"]:
                # build replace_tokens
                replace_tokens = []
                for i, token_len in enumerate(graph_token_lens):
                    replace_token = DEFAULT_GRAPH_PATCH_TOKEN * token_len
                    if graph_cfg['use_graph_start_end']:
                        replace_token = DEFAULT_G_START_TOKEN + replace_token + DEFAULT_G_END_TOKEN
                    replace_tokens.append(replace_token)

                for i, replace_token in enumerate(replace_tokens):
                    index = sentence["value"].find(DEFAULT_GRAPH_TOKEN)
                    sentence["value"] = sentence["value"][:index] + replace_token + sentence["value"][index+len(DEFAULT_GRAPH_TOKEN):]

    return sources

def preprocess_llama_3(
    sources,
    tokenizer: transformers.PreTrainedTokenizer,
) -> Dict:
    conv = conversation_lib.default_conversation.copy()
    roles = {"human": conv.roles[0], "gpt": conv.roles[1]}

    # Apply prompt templates
    conversations = []
    for i, source in enumerate(sources):
        if roles[source[0]["from"]] != conv.roles[0]:
            # Skip the first one if it is not from human
            source = source[1:]

        conv.messages = []
        for j, sentence in enumerate(source):
            role = roles[sentence["from"]]
            assert role == conv.roles[j % 2], f"{i}"
            conv.append_message(role, sentence["value"])
        conversations.append(conv.get_prompt())

    # Tokenize conversations

    input_ids = tokenizer(
        conversations,
        return_tensors="pt",
        padding="longest",
        max_length=tokenizer.model_max_length,
        truncation=True,
    ).input_ids

    targets = input_ids.clone()
    assert conv.sep_style == conversation_lib.SeparatorStyle.LLAMA_3

    # Mask targets
    sep = conv.sep + conv.roles[1]
    for conversation, target in zip(conversations, targets):
        total_len = int(target.ne(tokenizer.pad_token_id).sum())

        rounds = conversation.split(conv.sep)
        re_rounds = [conv.sep.join(rounds[:3])] # system + user + gpt
        for conv_idx in range(3, len(rounds), 2):
            re_rounds.append(conv.sep.join(rounds[conv_idx:conv_idx+2]))    # user + gpt
        
        # include <bos> for all rounds
        cur_len = 0
        # target[:cur_len] = IGNORE_INDEX
        for i, rou in enumerate(re_rounds):
            if rou == "":
                break

            parts = rou.split(sep)
            if len(parts) != 2:
                print(f"WARNING: parts!=: {parts}")
                break
            parts[0] += sep

            # not include <bos> for all rounds
            round_len = len(tokenizer(rou).input_ids)
            instruction_len = len(tokenizer(parts[0]).input_ids)

            # include <|eot_id|> for all rounds
            round_len += 1
            instruction_len += 1

            target[cur_len : cur_len + instruction_len] = IGNORE_INDEX
            cur_len += round_len

        target[cur_len:] = IGNORE_INDEX

        if cur_len < tokenizer.model_max_length:
            if cur_len != total_len:
                target[:] = IGNORE_INDEX
                print(
                    f"WARNING: tokenization mismatch: {cur_len} vs. {total_len}."
                    f" (ignored)"
                )

    return dict(
        input_ids=input_ids,
        labels=targets,
    )

